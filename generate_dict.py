def parse_raw(raw_text, pos_tag):
    words = []
    tokens = re.split(r'[\s,]+', raw_text.strip())
    for i in range(0, len(tokens)-1, 2):
        kanji = tokens[i]
        kana = tokens[i+1]
        vowels = get_vowels(kana)
        words.append({
            'w': kanji,
            'k': kana,
            'v': vowels,
            'p': pos_tag
        })
    return words

