def get_vowels(reading):
    VOWEL_MAP = {
        'ア':'a', 'イ':'i', 'ウ':'u', 'エ':'e', 'オ':'o',
        'カ':'a', 'キ':'i', 'ク':'u', 'ケ':'e', 'コ':'o',
        'サ':'a', 'シ':'i', 'ス':'u', 'セ':'e', 'ソ':'o',
        'タ':'a', 'チ':'i', 'ツ':'u', 'テ':'e', 'ト':'o',
        'ナ':'a', 'ニ':'i', 'ヌ':'u', 'ネ':'e', 'ノ':'o',
        'ハ':'a', 'ヒ':'i', 'フ':'u', 'ヘ':'e', 'ホ':'o',
        'マ':'a', 'ミ':'i', 'ム':'u', 'メ':'e', 'モ':'o',
        'ヤ':'a', 'ユ':'u', 'ヨ':'o',
        'ラ':'a', 'リ':'i', 'ル':'u', 'レ':'e', 'ロ':'o',
        'ワ':'a', 'ヲ':'o', 'ン':'n',
        'ガ':'a', 'ギ':'i', 'グ':'u', 'ゲ':'e', 'ゴ':'o',
        'ザ':'a', 'ジ':'i', 'ズ':'u', 'ゼ':'e', 'ゾ':'o',
        'ダ':'a', 'ヂ':'i', 'ヅ':'u', 'デ':'e', 'ド':'o',
        'バ':'a', 'ビ':'i', 'ブ':'u', 'ベ':'e', 'ボ':'o',
        'パ':'a', 'ピ':'i', 'プ':'u', 'ペ':'e', 'ポ':'o',
        'ァ':'a', 'ィ':'i', 'ゥ':'u', 'ェ':'e', 'ォ':'o',
        'ャ':'a', 'ュ':'u', 'ョ':'o', 'ッ':'x', 'ー':'-'
    }
    vowels = ""
    for c in reading:
        if c in ['ャ','ュ','ョ','ァ','ィ','ゥ','ェ','ォ']:
            if len(vowels) > 0:
                vowels = vowels[:-1] + VOWEL_MAP.get(c, '')
            else:
                vowels += VOWEL_MAP.get(c, '')
        elif c == 'ー':
            if len(vowels) > 0: vowels += vowels[-1]
        elif c in VOWEL_MAP:
            vowels += VOWEL_MAP[c]
    return vowels

