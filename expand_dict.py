def get_vowels(reading):
    vowels = ""
    for c in reading:
        if c in ['ャ','ュ','ョ','ァ','ィ','ゥ','ェ','ォ']:
            if len(vowels) > 0:
                vowels = vowels[:-1] + VOWEL_MAP.get(c, '')
            else:
                vowels += VOWEL_MAP.get(c, '')
        elif c == 'ー':
            if len(vowels) > 0: vowels += vowels[-1]
        elif c in VOWEL_MAP:
            vowels += VOWEL_MAP[c]
    return vowels

